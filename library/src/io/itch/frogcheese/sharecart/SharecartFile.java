package io.itch.frogcheese.sharecart;

import java.io.File;

/**
 * Created by Peter on 15-05-23.
 */
class ShareCartFile {

    private File file;
    private boolean autoCreated = false;

    /**
     * Creates an instance from the given file.
     *
     * @param file A sharecart file.
     * @return A ShareCartFile instance wrapping the given file. Note that there is no guarantee
     * that the sharecart file is valid at this point.
     */
    public static ShareCartFile fromFile(File file) {
        return new ShareCartFile(file);
    }

    /**
     * Creates an instance from the given file that was automatically generated by the framework.
     *
     * @param file The generated file.
     * @return A ShareCartFile instance wrapping the given file, with its {@link #isAutoCreated()} flag set to true.
     */
    public static ShareCartFile fromAutoCreatedFile(File file) {
        return new ShareCartFile(file, true);
    }

    private ShareCartFile(File file) {
        this(file, false);
    }

    private ShareCartFile(File file, boolean autoCreated) {
        if (file == null)
            throw new IllegalArgumentException("Cannot pass null as a file.");

        this.file = file;
        this.autoCreated = autoCreated;
    }

    public File getFile() {
        return this.file;
    }

    /**
     * Sets the flag specifying whether or not this file was automatically generated by the framework.
     *
     * @param autoCreated A flag.
     */
    public void setIsAutoCreated(boolean autoCreated) {
        this.autoCreated = autoCreated;
    }

    /**
     * @return Whether or not the file was automatically generated by the framework.
     */
    public boolean isAutoCreated() {
        return this.autoCreated;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        ShareCartFile that = (ShareCartFile) o;

        return file.equals(that.file);

    }

    @Override
    public int hashCode() {
        return file.hashCode();
    }
}
